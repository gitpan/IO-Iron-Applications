package IO::Iron::Applications::IronCache::Command::clear;

use 5.010_000;
use strict;
use warnings FATAL => 'all';

# Global creator
BEGIN {
    # Inheritance
    use parent qw( IO::Iron::Applications::Command::CommandBase );
}

# Global destructor
END {
}

# ABSTRACT: Clear a cache(s).

our $VERSION = '0.10'; # VERSION: generated by DZP::OurPkgVersion


use Data::Dumper;

use IO::Iron::Applications::IronCache -command;

sub description {
	return 'Clear a cache(s).';
}

sub usage_desc {
	my ($self, $opt, $args) = @_;
	return $opt->arg0() . ' %o clear item/cache <item_key>[,<item_key>]/<cache_name>[,<cache_name>] [--cache <cache_name>[,<cache_name>]]';}

sub opt_spec {
    # Note, dashes '-' become underscores '_' during opt_spec conversion!
	return (
        IO::Iron::Applications::Command::CommandBase::opt_spec_base(),
        [ 'cache=s', 'cache name or names (separated with \',\')', ],
	);
}

sub validate_args {
	my ($self, $opt, $args) = @_;
    $self->validate_args_base($opt, $args);
    $self->usage_error('invalid arguments') unless $args->[0] eq 'cache';
    return;
}

sub execute {
    my ($self, $opts, $args) = @_;

    $self->raise_logging_levels_from_options($opts);
    if($self->check_for_iron_io_config($opts)) {
        return 1;
    }
    my %parameters;
    $parameters{'config'} = $opts->{'config'} if defined $opts->{'config'};
    $parameters{'policies'} = $opts->{'policies'} if defined $opts->{'policies'};
    $parameters{'no-policy'} = $opts->{'no-policy'};
    my %output;
    $parameters{'cache_name'} = [ split m/[,]/msx, $args->[1] ]; # expects array
    %output = IO::Iron::Applications::IronCache::Functionality::clear_cache(%parameters);
    print $self->combine_template('clear_cache', \%output);
    return 0;
}

1;

